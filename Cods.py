import pandas as pd
from transformers import pipeline
from IPython.display import display

analisador = pipeline("sentiment-analysis", model="nlptown/bert-base-multilingual-uncased-sentiment")

comentarios = [
    "Adorei o produto, chegou super rápido e bem embalado!",
    "Péssimo. A caixa veio rasgada e faltavam peças. Quero o meu dinheiro de volta.",
    "Produto razoável. Pelo preço vale a pena, mas a qualidade não é a melhor.",
    "Ainda não recebi o meu pedido, estou muito chateado com a demora.",
    "Excelente atendimento da loja, recomendo a todos."
]

df_reviews = pd.DataFrame({'texto_desestruturado': comentarios})

resultados = analisador(comentarios)

df_reviews['feature_sentimento_ia'] = [res['label'] for res in resultados]
df_reviews['confianca_do_modelo'] = [round(res['score'], 2) for res in resultados]

display(df_reviews)