from pyspark.sql import SparkSession
from pyspark.sql.functions import col, year, month, to_timestamp

print("Iniciando o Pipeline de Dados (ETL) com Apache Spark...\n")

spark = SparkSession.builder.appName("OlistETL").getOrCreate()

df_bruto = spark.read.csv('/content/olist_orders_dataset.csv', header=True, inferSchema=True)

df_transformado = df_bruto.withColumn("data_compra", to_timestamp(col("order_purchase_timestamp"))) \
                          .withColumn("ano_compra", year(col("data_compra"))) \
                          .withColumn("mes_compra", month(col("data_compra"))) \
                          .filter(col("order_status") == 'delivered')

print("Transformação concluída! Amostra dos dados modelados prontos para análise:")
df_transformado.select("order_id", "order_status", "data_compra", "ano_compra", "mes_compra").show(5, truncate=False)

